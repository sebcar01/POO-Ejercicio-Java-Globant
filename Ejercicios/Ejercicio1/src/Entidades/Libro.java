package Entidades;

public class Libro {

    public String titulo;
    public String autor;
    public int paginas;
    public int isbn;

    public Libro() {
    }

    public Libro(String titulo, String autor, int paginas, int isbn) {
        this.titulo = titulo;
        this.autor = autor;
        this.paginas = paginas;
        this.isbn = isbn;
    }


}
